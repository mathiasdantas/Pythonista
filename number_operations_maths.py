# Operações Matemáticas
print(10 + 6)  # Adição
print(10 - 6)  # Subtração
print(10 * 6)  # Multiplicação
print(10 / 6)  # Divisão
print(10 // 6)  # divisão de inteiro
print(10 % 6)  # Resto da divisão // modulos
print(10 ** 6)  # exponenciais

# Atalho para atribuição mais rapida
a = 10
a = a + 5
a += 5  # Mais rapido

b = 20
b = b - 2
b -= 2  # Mais rapido

# Operações matemáticas comuns
print(round(5.7))  # Arredondar um valor
print(ceil(2.2))  # Forçar o arredondamento para o número mais alto

